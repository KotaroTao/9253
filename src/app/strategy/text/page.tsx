export default function StrategyTextPage() {
  const [copied, setCopied] = useState(false)

  const handleCopy = async () => {
    try {
      await navigator.clipboard.writeText(FULL_TEXT)
      setCopied(true)
      setTimeout(() => setCopied(false), 2000)
    } catch {
      // Fallback for older browsers
      const textarea = document.createElement("textarea")
      textarea.value = FULL_TEXT
      textarea.style.position = "fixed"
      textarea.style.opacity = "0"
      document.body.appendChild(textarea)
      textarea.select()
      document.execCommand("copy")
      document.body.removeChild(textarea)
      setCopied(true)
      setTimeout(() => setCopied(false), 2000)
    }
  }

  return (
    <div className="min-h-screen bg-background font-sans text-foreground">
      {/* Header */}
      <header className="sticky top-0 z-50 border-b bg-background/95 backdrop-blur">
        <div className="container flex h-14 items-center justify-between">
          <span className="text-lg font-bold text-gradient">MIERU Clinic</span>
          <button
            onClick={handleCopy}
            className={`inline-flex items-center gap-2 rounded-lg px-4 py-2 text-sm font-medium transition-colors ${
              copied
                ? "bg-emerald-100 text-emerald-700"
                : "bg-primary text-primary-foreground hover:bg-primary/90"
            }`}
          >
            {copied ? (
              <>
                <Check className="h-4 w-4" />
                コピー完了
              </>
            ) : (
              <>
                <Copy className="h-4 w-4" />
                全文コピー
              </>
            )}
          </button>
        </div>
      </header>

      {/* Content */}
      <main className="container max-w-3xl py-10">
        <pre className="whitespace-pre-wrap break-words text-sm leading-relaxed text-foreground/90">
          {FULL_TEXT}
        </pre>
      </main>
    </div>
  )
}
